package net.senior.digistask.model;

public class ColorModel {
        /**
         * From : Min
         * To : -110
         * Color : #000A00
         */

        private String From;
        private String To;
        private String Color;

        public String getFrom() {
            return From;
        }

        public void setFrom(String From) {
            this.From = From;
        }

        public String getTo() {
            return To;
        }

        public void setTo(String To) {
            this.To = To;
        }

        public String getColor() {
            return Color;
        }

        public void setColor(String Color) {
            this.Color = Color;
        }
    }
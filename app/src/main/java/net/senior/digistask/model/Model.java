public class Model {


    /**
     * RSRP : -71
     * RSRQ : -19
     * SINR : 22
     */

    private float RSRP;
    private float RSRQ;
    private float SINR;

    public float getRSRP() {
        return RSRP;
    }

    public void setRSRP(float RSRP) {
        this.RSRP = RSRP;
    }

    public float getRSRQ() {
        return RSRQ;
    }

    public void setRSRQ(float RSRQ) {
        this.RSRQ = RSRQ;
    }

    public float getSINR() {
        return SINR;
    }

    public void setSINR(float SINR) {
        this.SINR = SINR;
    }
}
